public class Item {

    private String name;
    private double price;

    public Item(String name, double price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }


}

/*
    The 'Item' class represents a single item that can be added  to the cart, and it stores
    the name and price of the item.
 */
